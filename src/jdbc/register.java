    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String username = txt_username.getText();
        String password = txt_password.getText();
        
        if(password.equals("")||username.equals("")){
            JOptionPane.showMessageDialog(null, "Username atau Password tidak boleh kosong!");
        }else{
        try {
            String sql = "INSERT INTO users (username, password) VALUES ('"+username+"','"+password+"')";
            java.sql.Connection conn =(Connection)Jdbc.configDB();
            java.sql.Statement pst = conn.createStatement();
            pst.execute(sql);
            JOptionPane.showMessageDialog(null, "Berhasil mendaftarkan User");
        
        } catch(Exception e){
                  JOptionPane.showMessageDialog(null, "Username Sudah Digunakan!");
                  System.out.println(e.getMessage());
            }
            finally{
                this.setVisible(false);
                new Login().setVisible(true);
               
            }  
        }
    }//GEN-LAST:event_jButton2ActionPerformed

